package com.lanou3g.sudy;

public class Cat {
		private String name;
		private Person owner;
		
		public void setName(String name) {
			this.name = name;
		}
		public String getName() {
			return name;
		}
		public Person getOwner() {
			return owner;
		}
		public void setOwner(Person owner) {
			this.owner = owner;
		}
		
		
		//构造方法
		/*
		 * 构造方法:
		 *   1.方法名与类名一样
		 *   2.没有返回值这一功能
		 *   
		 *   
		 *   有了构造方法:.还要set,get 干嘛?
		 *   首先第一点:
		 *   get是一定要有的.
		 *   否则类的外面无法获得私有属性的
		 *   
		 *  关于set
		 *    有些属性,并不是每个对象都要具有的...
		 *    比如cat可能会有一个伴侣,但是并不是每一个cat
		 *    cat在初始化的时候,都马上有一个伴侣
		 *    所以在后续的使用中,通过set方法
		 *    给他设置一个
		 *    对于初始化就有的这个owner属性,
		 *    很可能这个猫也后被卖了,那就需要换一个
		 *    主人,这个时候后也可以使用set方法,去修改属性
		 *  
		 *   
		 */
		public Cat() {
			System.out.println("我是无参的构造方法");
		}
		
		
		public Cat(Person owner) {
			//将括号中 的参数owner 的值
			//赋值给属性的owner
			//没有返回值这个功能
			
			this.owner = owner;
		}
		
		
		
		
		public void showOwner() {
			System.out.println("我的主人是  :" + owner.getName());
		}
}
